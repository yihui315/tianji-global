// ─── Helper: Look up a JiaZi entry by stem and branch indices ─────────────────

export function getJiaZiEntry(stemIndex: number, branchIndex: number): JiaZiEntry | undefined {
  return JIAZI_COMBINATIONS.find(
    (e) => e.stemIndex === stemIndex && e.branchIndex === branchIndex
  );
}

// ─── Helper: Look up stem meaning ─────────────────────────────────────────────

export function getStemMeaning(stem: string): StemMeaning | undefined {
  return STEM_MEANINGS.find((s) => s.stem === stem);
}

// ─── Helper: Look up branch meaning ───────────────────────────────────────────

export function getBranchMeaning(branch: string): BranchMeaning | undefined {
  return BRANCH_MEANINGS.find((b) => b.branch === branch);
}

// ─── Helper: Get element cycle info ────────────────────────────────────────────

export function getElementCycle(element: string): ElementCycleEntry | undefined {
  return ELEMENT_CYCLE.find((e) => e.element === element);
}

// ─── Build KB text for embedding/search ──────────────────────────────────────

/**
 * Convert the BaZi KB into a flat text corpus for embedding.
 * One line per entry — easy for RAG retrieval.
 */
export function buildBaZiCorpus(): string[] {
  const lines: string[] = [];

  for (const entry of JIAZI_COMBINATIONS) {
    lines.push(
      `JiaZi ${entry.jiaziName}: ${entry.yearNature_zh} | ${entry.yearNature_en}. ` +
      `Element: ${entry.element_zh} (${entry.element_en}). ` +
      `Personality: ${entry.personality_zh} | ${entry.personality_en}. ` +
      `Strengths: ${entry.strengths_zh} | ${entry.strengths_en}. ` +
      `Weaknesses: ${entry.weaknesses_zh} | ${entry.weaknesses_en}. ` +
      `Fortune: ${entry.fortune_zh} | ${entry.fortune_en}.`
    );
  }

  for (const s of STEM_MEANINGS) {
    lines.push(
      `Stem ${s.stem}: ${s.name_zh} (${s.name_en}). ` +
      `Element: ${s.element_zh} (${s.element_en}). ` +
      `Nature: ${s.nature_zh} | ${s.nature_en}. ` +
      `Direction: ${s.direction_zh} (${s.direction_en}).`
    );
  }

  for (const b of BRANCH_MEANINGS) {
    lines.push(
      `Branch ${b.branch}: ${b.name_zh} ${b.zodiac_zh} (${b.name_en}). ` +
      `Element: ${b.element_zh} (${b.element_en}). ` +
      `Hour: ${b.hour_range}. Month: ${b.month_position}. ` +
      `Direction: ${b.direction_zh} (${b.direction_en}).`
    );
  }

  return lines;
}
