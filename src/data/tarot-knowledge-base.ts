// ─── All Cards Array ──────────────────────────────────────────────────────────

export const TAROT_CARDS: TarotCard[] = [
  ...TAROT_MAJOR_ARCANA.map((c) => ({ ...c, suit: undefined })),
  ...TAROT_WANDS,
  ...TAROT_CUPS,
  ...TAROT_SWORDS,
  ...TAROT_PENTACLES,
];

// ─── Lookup Helper ────────────────────────────────────────────────────────────

export function getCardMeaning(
  cardId: string,
  isReversed: boolean = false
): TarotCard | undefined {
  const card = TAROT_CARDS.find((c) => c.id === cardId);
  return card;
}

export function getCardByName(name: string): TarotCard | undefined {
  return TAROT_CARDS.find(
    (c) =>
      c.name_en.toLowerCase() === name.toLowerCase() ||
      c.name_zh === name
  );
}

// ─── Build Corpus ────────────────────────────────────────────────────────────

export function buildTarotCorpus(): TarotCorpusEntry[] {
  return TAROT_CARDS.map((card) => ({
    id: card.id,
    content:
      `Tarot ${card.arcana === 'major' ? 'Major' : card.suit} — ${card.name_en} (${card.name_zh}): ` +
      `Upright: ${card.keywords_upright_en.join(', ')} | ${card.keywords_upright_zh.join(', ')}. ` +
      `Meaning: ${card.meaning_upright_en} | ${card.meaning_upright_zh}. ` +
      `Reversed: ${card.keywords_reversed_en.join(', ')} | ${card.keywords_reversed_zh.join(', ')}. ` +
      `Reversed Meaning: ${card.meaning_reversed_en} | ${card.meaning_reversed_zh}. ` +
      `Symbolism: ${card.symbolism_en} | ${card.symbolism_zh}.`,
    source: `tarot-${card.arcana}-${card.suit ?? 'major'}`,
  }));
}
